void main() {
  String a;

  a = '10';

  switch (a) {
    case '1':
      print('O numero é igual a 1');
      break;
    case '2':
      print('O numero é igual a 2');
      break;
    case '3':
      print('O numero é igual a 3');
      break;
    case '4':
      print('O numero é igual a 4');
      break;
    default:
      print('Outro numero');
  }
}
