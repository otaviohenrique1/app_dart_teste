void main() {
  // Lista de operadores
  // == -> igual (comparacao)
  // <= -> menor ou igual
  // >= -> maior ou igual
  // != -> diferente
  // < -> menor
  // > -> maior
  // = -> igual (atribuicao) (nao confundir com o == (comparacao))

  int a = 1;

  if (a == 1) {
    print('O numero é igual a 1');
  } else if (a == 0) {
    print('O numero é igual a 0');
  } else {
    print('O numero é diferente de 1');
  }
}
